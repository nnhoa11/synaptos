import jsPDF from "jspdf";
import autoTable from "jspdf-autotable";

export function generateSDGReportPDF(metrics, storeName, period) {
  const doc = new jsPDF();

  doc.setFontSize(16).setFont(undefined, "bold");
  doc.text("SynaptOS - SDG 12 Report", 14, 20);
  doc.setFontSize(10).setFont(undefined, "normal");
  doc.text(`Store: ${storeName}`, 14, 30);
  doc.text(`Period: ${period}`, 14, 36);
  doc.text("Responsible Consumption and Production", 14, 42);

  doc.setFontSize(11).setFont(undefined, "bold");
  doc.text(`Waste rate: ${(Number(metrics.wasteRate ?? 0) * 100).toFixed(1)}%`, 14, 54);
  doc.text(`Baseline: ${(Number(metrics.baselineWasteRate ?? 0) * 100).toFixed(1)}%`, 14, 60);
  doc.text(`Kg diverted: ${Number(metrics.totalKgDiverted ?? 0).toLocaleString()}`, 14, 66);
  doc.text(`CO2 saved: ${Number(metrics.co2SavedKg ?? 0).toLocaleString()} kg`, 14, 72);

  autoTable(doc, {
    startY: 82,
    head: [["Action", "Count"]],
    body: [
      ["Markdown rescue", Number(metrics.breakdown?.markdown_rescue ?? 0).toLocaleString()],
      ["Cross-dock", Number(metrics.breakdown?.cross_dock ?? 0).toLocaleString()],
      ["EOL donation", Number(metrics.breakdown?.eol_donation ?? 0).toLocaleString()],
      ["EOL compost", Number(metrics.breakdown?.eol_compost ?? 0).toLocaleString()],
    ],
  });

  doc.setFontSize(10).setFont(undefined, "normal");
  doc.text(
    "Generated by SynaptOS Agentic AI Platform in support of Decision 222/QD-TTg circular-economy compliance.",
    14,
    doc.lastAutoTable.finalY + 16,
    { maxWidth: 180 }
  );

  doc.save(`sdg-report-${period}.pdf`);
}
